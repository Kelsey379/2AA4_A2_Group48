package ca.mcmaster.se2aa4.island.teamXXX;

public class PointOfInterest {
    private double x;
    private double y;

    public PointOfInterest(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() { 
        return x; 
    }
    public double getY() { 
        return y; 
    }
}